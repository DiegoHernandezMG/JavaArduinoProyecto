    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        // TODO add your handling code here:
        try{
            int codigo = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el c칩digo de seguridad de su tarjeta"));
            
            if(UserD.validarCvv(idUser, codigo)){
                CarritoDao.pagarCarrito(idUser);
                CarritoDao.crearTicket(idUser,txtTotal.getText());
                CarritoDao.vaciarCarro(idUser);
                mostrarCarrito();
                calcularPrecio();
            }
            else{
            JOptionPane.showMessageDialog(null, "El c칩digo que ingreso "
                + "es incorrecto, intente de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
            }     
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Dato invalido" + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarActionPerformed

