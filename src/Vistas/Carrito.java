    public void mostrarCarrito(){
        CrearTabla objTabla = new CrearTabla();
        DefaultTableModel modelo = objTabla.mostrarCarrito(idUser);
        tableCarrito.setDefaultRenderer(Object.class, new TablaImagen());
        tableCarrito.setRowHeight(64);
        tableCarrito.setModel(modelo);
    }
    
    public void calcularPrecio(){
        int n = tableCarrito.getRowCount();
        float total = 0;
        
        for(int i=0;i<n;i++){
            String valor = tableCarrito.getValueAt(i, 2).toString();
            String cantidad = tableCarrito.getValueAt(i, 3).toString();
            total += Float.parseFloat(valor.substring(1)) * Float.parseFloat(cantidad);   
        }
        txtTotal.setText("$" + Float.toString(total) + "0");
    }
